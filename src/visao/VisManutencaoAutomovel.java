    /**
    *
    * Método responsável por buscar os Multas no Banco e inseri-los na JTable
    * 
    * @version 2.3 beta
    * @author Matheus Souza da Silva
    * @since 03/05/2011
    * @exception Exception Lança uma excessao se algum erro ocorrer
    */
    private void inserirTodosMultasNaTabela()
    {
        try 
        {
            DefaultTableModel dtm = (DefaultTableModel) jTableMulta.getModel();
            String[] linha = new String[6];

            NegAutomovel ac = new NegAutomovel();
            ArrayList<PerAutomovel> ab = new ArrayList<PerAutomovel>();
            ab = ac.buscarTodosAutomoveis();

            //Zerar número de linhas da tabela
            dtm.setNumRows(0);

            //Preenchimento da JTable
            for (int i=0; i<ab.size(); i++) {
                linha[0] = Integer.toString(ab.get(i).getIdautomovel());
                linha[1] = ab.get(i).getMarca();
                linha[2] = ab.get(i).getModelo();
                linha[3] = Integer.toString(ab.get(i).getAno());
                linha[4] = ab.get(i).getPlaca();
                linha[5] = ab.get(i).getMulta().getInfracao();
                dtm.addRow(linha);
            }//for (int i=0; i<ab.size(); i++) {

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro! "+e.getMessage());
        }//try
    }//private void inserirTodosMultasNaTabela()

