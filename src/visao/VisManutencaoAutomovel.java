    /**
    *
    * Método que tem finalidade de deletar o automovel
    * 
    * @version 2.3 beta
    * @author Matheus Souza da Silva
    * @since 03/05/2011
    * @exception Exception Lança uma excessao se algum erro ocorrer
    */
    private void jbDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarActionPerformed
        try 
        {
            if (jTableMulta.getSelectedRow() != -1) {
                /* Utilizamos o getSelectedRow() para retornar o número
                da linha que o usuário selecionou */
                int linhaSelecionada = jTableMulta.getSelectedRow();
                
                /* O método getValueAt(linha,coluna) retorna o valor de cada coluna
                que foi selecionada. O toString converte os valores
                para String */
                String coluna1 = jTableMulta.getModel().getValueAt(linhaSelecionada, 0).toString();
                String coluna2 = jTableMulta.getModel().getValueAt(linhaSelecionada, 1).toString();
                String coluna3 = jTableMulta.getModel().getValueAt(linhaSelecionada, 2).toString();
                String coluna4 = jTableMulta.getModel().getValueAt(linhaSelecionada, 3).toString();
                String coluna5 = jTableMulta.getModel().getValueAt(linhaSelecionada, 4).toString();
                
                /* Inserindo as colunas em um objeto do tipo
                PerAutomovel */
                PerAutomovel a = new PerAutomovel();
                a.setIdautomovel(Integer.parseInt(coluna1));
                a.setMarca(coluna2);
                a.setModelo(coluna3);
                a.setAno(Integer.parseInt(coluna4));
                a.setPlaca(coluna5);

                /* Instanciando um NegAutomovel e enviando
                o objeto selecionado para o método:
                deletarAutomovelCodigo */
                NegAutomovel ac = new NegAutomovel();
                
                if (ac.deletarAutomovelCodigo(a) == false) {
                    JOptionPane.showMessageDialog(this, ac.erros+"\nfalha ao Excluír Automovel!");
                    // Chamando método para atualizar os dados da JTable 
                    this.inserirTodosMultasNaTabela();
                } else {
                    // Chamando método para atualizar os dados da JTable 
                    this.inserirTodosMultasNaTabela();

                    JOptionPane.showMessageDialog(this, "Automovel Excluído com Sucesso!");
                }//if (ac.deletarAutomovelCodigo(a) == false) {

            } else {
                 JOptionPane.showMessageDialog(
                    this,
                    "Você não selecionou nenhum Automovel",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
                 );
            }//if (jTableMulta.getSelectedRow() != -1) {
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro! "+e.getMessage());
        }//try
    }//GEN-LAST:event_jbDeletarActionPerformed

