/**
*
* Método que tem finalidade de salvar as alterações do automovel
* 
* @version 2.3 beta
* @author Matheus Souza da Silva
* @since 03/05/2011
* @exception Exception Lança uma excessao se algum erro ocorrer
*/
private void jbSalvarAlteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarAlteracoesActionPerformed
    try
    {
        PerAutomovel a = new PerAutomovel();
        NegAutomovel ac = new NegAutomovel();

        /* Buscamos os valores que o usuário alterou nos
        JTextField, montamos um objeto do tipo PerAutomovel
        e enviamos para o método atualizarAutomovelCodigo() */
        a.setIdautomovel(Integer.parseInt(jtCodigo.getText()));
        a.setMarca(jtMarca.getText());
        a.setModelo(jtModelo.getText());
        a.setAno(Integer.parseInt(jtAno.getText()));
        a.setPlaca(jtPlaca.getText());

        ac.atualizarAutomovelCodigo(a);
        limpar();

        this.inserirTodosMultasNaTabela();

        JOptionPane.showMessageDialog(this, "Automovel Alterado com Sucesso! ");
        desabilita(); 
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro! "+e.getMessage());
    }//try
}//GEN-LAST:event_jbSalvarAlteracoesActionPerformed

